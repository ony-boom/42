#include <unistd.h>

int	**rejet_map(int **map)
{
	int	i1;
	int	i2;

	i1 = 0;
	while (i1 < 4)
	{
		i2 = 0;
		while (i2 < 4)
		{
			map[i1][i2] = 0;
			i1++;
			i2++;
		}
	}
	return (map);
}
